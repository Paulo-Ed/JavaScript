// JavaScript possui tipagem dinâmica, sendo fracamente tipada, ou seja, uma variável é capaz de armazenar dados de diferentes tipos ao longo do tempo.

let variavelUm = 700 //variável como number

console.log(variavelUm)
console.log(typeof variavelUm)

variavelUm = "Olá Mundo" //variável como string

console.log(variavelUm)
console.log(typeof variavelUm)
