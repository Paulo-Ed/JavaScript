//Atribuição de valor
let numero1 = 40
let numero2 = 60

//Atribuição com operações aritméticas
numero1 += numero2 //numero1 = numero1 + numero2
console.log(numero1)

numero1 -= numero2 //numero1 = numero1 - numero2
console.log(numero1)

numero1 *= 2 //numero1 = numero1 * 2
console.log(numero1)

numero1 /= 2 //numero1 = numero1 / 2
console.log(numero1)

numero1 %= numero2 //numero1 = numero1 % 2
console.log(numero1)

numero1 **= 3 //numero1 * numero1 * numero1
console.log(numero1)