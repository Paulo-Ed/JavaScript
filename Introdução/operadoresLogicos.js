//Os operadores lógicos estabelecem uma relação
let teste = true

console.log("Ou:",teste || teste) //Operador OU 
console.log("E:",teste && teste) //Operador E
console.log("Negação:",!teste) //Operador de Negação
console.log("Ou Exclusivo:",!teste || teste) //Artifício para criar um operador de OU Exclusivo